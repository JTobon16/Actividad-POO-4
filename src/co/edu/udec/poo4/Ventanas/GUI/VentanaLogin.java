    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
      //recuperamos datos del formulario
      String cedula = campoCedula.getText();
      char clave [ ] = campoClave.getPassword();
      String password = String.valueOf(clave);
      
      //buscar cedula entre las claves del diccionario o map
      Usuario user = null;
      Map usuariosGYM = Usuario.usuariosGYM;
      if (usuariosGYM == null){
              textMensaje.setForeground(Color.RED);
              JOptionPane.showMessageDialog(this,"Base de Datos vacia, registrese");
              campoCedula.setText("");
              campoClave.setText(""); 
              dispose();          
            }else if( usuariosGYM.containsKey(cedula)){
                user = Usuario.usuariosGYM.get(cedula);
                if(user.password.equals(password)){
                textMensaje.setForeground(Color.GREEN);
                JOptionPane.showMessageDialog(this,"Saludos: "+user.nombre);
                Usuario.UsuarioActual = user;
                dispose();
            }  
            else {  
              textMensaje.setForeground(Color.RED);
              JOptionPane.showMessageDialog(this,"Acceso Negado");
              campoCedula.setText("");
              campoClave.setText("");
                }       
          }else{
              JOptionPane.showMessageDialog(this,"Usuario no existe");
              campoCedula.setText("");
              campoClave.setText("");}
    }//GEN-LAST:event_botonAceptarActionPerformed

